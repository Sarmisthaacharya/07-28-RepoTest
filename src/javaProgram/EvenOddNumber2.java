package javaProgram;

public class EvenOddNumber2 {

	public static void main(String[] args) {

		int[] a={10,35,50,50,70};
		int len=a.length;
		 for(int i=0; i<len; i++)
		 {
		
		if(a[i]%2==0)
		{
		System.out.println("even number");
		}
		else
		{
		System.out.println("odd number");
		}
			
		 }

		

	}
	
	

}
